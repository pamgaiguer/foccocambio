						<strong>Como utilizar o seu cartão pré-pago</strong><br>
						<br>
						<strong>Como realizar saques e compras?</strong><br>
						Para compras em estabelecimentos credenciados:<br>
						Basta passar o cartão e digitar sua senha ou assinar o comprovante de compra no estabelecimento credenciado Visa. Por se tratar de um cartão pré-pago, em alguns países a transação será passada na função débito e em outros 
						na função crédito, porém, todas as transações serão sempre debitadas automaticamente do saldo disponível.<br>
						<br>
						<strong>Para compras na internet:</strong><br>
						Basta digitar, no site de compras, os dados que poderão ser solicitados:<br>
						<strong>.</strong> Número do cartão (16 dígitos localizados na frente do cartão);<br>
						<strong>.</strong> Validade do cartão (impressa na frente do cartão – MM/AA);<br>
						<strong>.</strong> Código de segurança (3 dígitos impressos no verso do cartão, próximos à tarja magnética).<br>
						<strong>.</strong> Nome completo do portador*;<br>
						<strong>.</strong> Endereço completo**.<br>
						<span class="interna-corcinza">*Aceitação do cartão pelo site na internet pode variar de acordo com a loja virtual, por isso, algumas transações podem ser recusadas.<br> 
						Transações realizadas em moeda diferente da moeda do seu cartão serão convertidas seguindo as taxas de conversão da Visa. <br>
						As transações serão autorizadas somente se houver saldo disponível no cartão.</span>
						</p>
						<br>
						<p class="interna-corazul">
						<strong>Para saques</strong><br>
						Insira o cartão em um caixa automático com selo da rede Plus, escolha a função crédito ou “não correntista”, digite a sua senha e siga as demais instruções. 
						Verificar sempre o limite máximo permitido em cada caixa automático, que pode varias de acordo com o local e horário. 
						Para cada saque efetuado, o valor de 2,50 na moeda do cartão é debitado automaticamente do seu cartão.
						<br><br>
						 
						<strong>Como são efetuados os débitos referentes a saques e compras?</strong><br>
						Todas as transações são debitadas na moeda do cartão (dólar americano, euro, libra esterlina, dólar australiano, dólar canadense ou peso argentino). 
						Transações realizadas onde a moeda local é diferente da moeda do seu cartão serão convertidas seguindo as taxas de conversão da Visa.
