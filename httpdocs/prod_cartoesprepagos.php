						<strong>Pagamento em Reais no exterior</strong><br>
						Alguns estabelecimentos no exterior passaram a oferecer a opção de pagamento em REAIS com cartão. Essa opção é escolhida por você no momento da compra.<br>
						Ao assinar o comprovante ou digitar a senha, tendo escolhido o pagamento em REAIS, você estará concordando com a taxa de conversão estabelecida pelo próprio estabelecimento.<br>
						Ao realizar compras em estabelecimentos que solicitam assinatura de contrato, fique atento às condições, pois você poderá 
						estar aceitando efetuar o pagamento em reais descrito em alguma cláusula, sendo assim, para evitar 
						gastos e tarifas indesejadas, pedimos que dê preferência para o pagamento na moeda local do país em que estiver ao utilizar o seu Cartão Visa Travel Money.
						<br>
						<br>
						<strong>Pagamentos em cruzeiros marítimos</strong><br>
						A utilização do cartão pode ser restrita em alguns cruzeiros marítimos em função de seus procedimentos internos. <br>
						Lembre-se de consultar previamente a aceitação do cartão no local da viagem. <br>
						<br>
						<strong>Compras com Gorjetas</strong><br>
						Ao realizar compras com o cartão verifique se o estabelecimento acrescentou a gorjeta (em inglês, “Tip") ao valor da transação. <br>
						Nunca assine o comprovante sem o preenchimento devido desta cobrança, que é opcional, caso não concorde, inutilize o espaço destinado à gorjeta.<br>
						Ao assinar o comprovante ou digitar a senha, você estará concordando com este valor extra que será debitado diretamente do saldo do seu cartão.
						<br><br>
						<strong>Posso fazer pré-reserva de hotéis e locadora de veículos?</strong><br>
						O seu cartão pode ser utilizado para pré-reserva e pagamento de hotel ou de aluguel de veículos, mas o saldo ficará bloqueado no valor da pré-reserva ou pagamento, acrescido de uma tarifa de 15% de segurança sobre esse valor 
						por um prazo de até 15 dias corridos, até que o estabelecimento confirme a transação.<br>
						A responsabilidade por essa transação, assim como pelo seu cancelamento, se for o caso, é exclusivamente do estabelecimento, já que este é o único que tem a possibilidade de efetuar lançamentos de débito no cartão.	
