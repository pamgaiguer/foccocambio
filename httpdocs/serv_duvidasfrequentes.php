					<p class="interna-corazul">
						<strong>Como devo proceder para comprar moeda estrangeira para turismo? </strong><br>
						Primeiramente, certifique-se que seu CPF esteja com a situação cadastral regular junto à Receita Federal.
						Disponibilizamos através deste site ou pelos telefones cotações de moedas atualizadas e atendimento personalizado 
						o esclarecimento necessário quanto aos procedimentos legais, ou seja, procedimentos regulamentados pelo Banco Central.
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Devo declarar o quanto estou levando em moeda estrangeira na minha saída do país? E na volta?</strong><br>
						As autoridades podem, na sua saída, solicitar o seu boleto de compra de moedas estrangeiras. Na sua volta, as 
						autoridades distribuem, ainda no avião, um formulário, com várias perguntas, dentre elas se você está portando mais 
						de US$10.000,00 (dez mil dólares). A partir de Abril de 2006, se tornou obrigatória a apresentação da E-DPV, disponível no site da Receita Federal.
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Há alguma taxa ou comissão que eu tenha que pagar além do preço?</strong><br>	
						Na Treviso Corretora de Câmbio S/A, a taxa fornecida no site é o valor da moeda que deverá ser diminuído do IOF 
						(0,38%) caso o cliente esteja vendendo ou acrescido do IOF (0,38%) em caso de compra. Ao comprar moedas 
						estrangeiras em outro estabelecimento, verifique se, além do preço, são cobrados custos adicionais (como por 
						exemplo taxas, comissões, seguro etc.).
						Para o serviço de Delivery, entregamos acima de 500,00 USD (quinhentos dólares americanos) sem custo adicional 
						para São Paulo - Capital (consulte a área de abrangência com nossos atendentes).
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Há limite para solicitações de moeda estrangeira para turismo? Quais documentos devo apresentar? </strong><br>
						Não há limite legal para a compra ou venda desde que exista origem dos recursos negociados. Você poderá comprar 
						ou vender a quantidade que desejar, embora quem estiver lhe vendendo estará avaliando cada caso. 
						Verifique a documentação necessária, com a equipe Focco.
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Porque existe diferença entre a taxa que está no jornal e a que está sendo vendida? </strong><br>
						Primeiro: a taxa do jornal sempre reflete a média do dia anterior. 
						Segundo: a taxa geralmente é a do dólar comercial (importação e exportação) que serve para sinalizar a venda e a compra diária. 
						Embora essa taxa possa variar durante o dia, ela é a base para o dólar flutuante, onde se enquadra o dólar turismo, pagamentos etc. 
						A taxa do dólar turismo é um pouco mais alta do que a do comercial. 
						Se você consultou a taxa do dólar comercial, você encontrará a diferença a maior.
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Quais as vantagens de comprar moeda estrangeira para turismo na Focco Câmbio e Turismo? </strong><br>
						A garantia de que você estará comprando moedas estrangeiras de um estabelecimento autorizado pelo 
						Banco Central, não havendo risco de serem falsas. Você estará recebendo o Boleto (nota) de 
						Venda que lhe permitirá sair legalizado do país e que poderá ser exigido a qualquer momento na hora da viagem.						
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Se eu efetuar o câmbio para comprar dólar agora e for viajar somente no próximo ano, devo declarar no meu imposto de renda? </strong><br>
						Depende da quantia que você comprou e o que representa esse valor na sua declaração de I.R. Se o valor for significativo para sua 
						declaração, deve constar dos seus bens a exemplo de um Fundo, CDB ou Ações.
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>A empresa necessita mandar um diretor ou funcionário para o exterior. Como proceder? </strong><br>						
						No caso do viajante ser funcionário ou contratado da empresa, o Boleto (nota) de Compra de moeda estrangeira deverá 
						ser feito em nome da empresa. Haverá um campo no boleto para informação do viajante (nome, endereço, CPF etc.). 
						A empresa deverá fazer uma carta declarando que o funcionário esta viajando à serviço da empresa.
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>O funcionário está no exterior e a empresa necessita lhe enviar uma certa quantia em moeda estrangeira. Como proceder? </strong><br>
						Sugerimos a compra do cartão pré-pago Visa Travel Money, na moeda mais conveniente.						
					</p>
					<hr>
					<p class="interna-corazul">
						<strong>Vou viajar pela empresa. Como proceder? </strong><br>
						Se você vai viajar pela empresa e a mesma lhe deu Reais para converter em moeda estrangeira, você deverá solicitar a moeda em seu nome. 
						Neste caso, o cheque de sua empresa deverá ser depositado em sua conta e você deve efetuar a compra em espécie, DOC ou TED. 
						Se, entretanto, a empresa está lhe comprando os dólares, proceda como na resposta à pergunta posterior.					
					</p>
